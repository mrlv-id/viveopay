import * as React from "react"
import { cn } from "@/lib/utils"
import { ChevronRight } from "lucide-react"
import { motion, useScroll, useTransform } from "framer-motion"

interface HeroSectionProps extends React.HTMLAttributes<HTMLDivElement> {
  title?: string
  subtitle?: {
    regular: string
    gradient: string
  }
  description?: string
  ctaText?: string
  ctaHref?: string
  onCtaClick?: () => void
  bottomImage?: {
    light: string
    dark: string
  }
  gridOptions?: {
    angle?: number
    cellSize?: number
    opacity?: number
    lightLineColor?: string
    darkLineColor?: string
  }
}

const RetroGrid = ({
  angle = 65,
  cellSize = 60,
  opacity = 0.5,
  lightLineColor = "gray",
  darkLineColor = "gray",
}) => {
  const gridStyles = {
    "--grid-angle": `${angle}deg`,
    "--cell-size": `${cellSize}px`,
    "--opacity": opacity,
    "--light-line": lightLineColor,
    "--dark-line": darkLineColor,
  } as React.CSSProperties

  return (
    <div
      className={cn(
        "pointer-events-none absolute size-full overflow-hidden [perspective:200px]",
        `opacity-[var(--opacity)]`,
      )}
      style={gridStyles}
    >
      <div className="absolute inset-0 [transform:rotateX(var(--grid-angle))]">
        <div className="animate-grid [background-image:linear-gradient(to_right,var(--light-line)_1px,transparent_0),linear-gradient(to_bottom,var(--light-line)_1px,transparent_0)] [background-repeat:repeat] [background-size:var(--cell-size)_var(--cell-size)] [height:300vh] [inset:0%_0px] [margin-left:-200%] [transform-origin:100%_0_0] [width:600vw] dark:[background-image:linear-gradient(to_right,var(--dark-line)_1px,transparent_0),linear-gradient(to_bottom,var(--dark-line)_1px,transparent_0)]" />
      </div>
      <div className="absolute inset-0 bg-gradient-to-t from-background to-transparent to-90%" />
    </div>
  )
}

const HeroSection = React.forwardRef<HTMLDivElement, HeroSectionProps>(
  (
    {
      className,
      title = "Build products for everyone",
      subtitle = {
        regular: "Designing your projects faster with ",
        gradient: "the largest figma UI kit.",
      },
      description = "Sed ut perspiciatis unde omnis iste natus voluptatem accusantium doloremque laudantium, totam rem aperiam, eaque ipsa quae.",
      ctaText = "Browse courses",
      ctaHref = "#",
      onCtaClick,
      bottomImage,
      gridOptions,
      ...props
    },
    ref,
  ) => {
    const containerRef = React.useRef<HTMLDivElement>(null);
    const { scrollYProgress } = useScroll({
      target: containerRef,
      offset: ["start start", "end start"]
    });

    const backgroundY = useTransform(scrollYProgress, [0, 1], ["0%", "50%"]);
    const gridY = useTransform(scrollYProgress, [0, 1], ["0%", "30%"]);
    const contentY = useTransform(scrollYProgress, [0, 1], ["0%", "20%"]);
    const opacity = useTransform(scrollYProgress, [0, 0.5, 1], [1, 0.8, 0.3]);

    return (
      <div className={cn("relative", className)} ref={ref} {...props}>
        <div ref={containerRef} className="relative overflow-hidden">
          <motion.div 
            className="absolute top-0 z-[0] h-screen w-screen bg-primary/5 bg-[radial-gradient(ellipse_20%_80%_at_50%_-20%,hsl(var(--primary)/.15),rgba(255,255,255,0))]"
            style={{ y: backgroundY }}
          />
          <motion.section 
            className="relative max-w-full mx-auto z-1"
            style={{ y: gridY }}
          >
            <RetroGrid {...gridOptions} />
            <motion.div 
              className="max-w-screen-xl z-10 mx-auto px-4 py-28 gap-12 md:px-8"
              style={{ y: contentY, opacity }}
            >
              <div className="space-y-5 max-w-3xl leading-0 lg:leading-5 mx-auto text-center">
                <h1 className="text-sm text-muted-foreground group font-sans mx-auto px-5 py-2 bg-gradient-to-tr from-muted/40 via-muted/30 to-transparent border-[2px] border-border/30 rounded-3xl w-fit">
                  {title}
                  <ChevronRight className="inline w-4 h-4 ml-2 group-hover:translate-x-1 duration-300" />
                </h1>
                <h2 className="text-4xl tracking-tighter font-sans bg-clip-text text-transparent mx-auto md:text-6xl bg-gradient-to-b from-foreground to-foreground/60">
                  {subtitle.regular}
                  <span className="text-transparent bg-clip-text bg-gradient-to-r from-primary to-primary/60">
                    {subtitle.gradient}
                  </span>
                </h2>
                <p className="max-w-2xl mx-auto text-muted-foreground">
                  {description}
                </p>
                <div className="items-center justify-center gap-x-3 space-y-3 sm:flex sm:space-y-0">
                  <span className="relative inline-block overflow-hidden rounded-full p-[1.5px]">
                    <span className="absolute inset-[-1000%] animate-[spin_2s_linear_infinite] bg-[conic-gradient(from_90deg_at_50%_50%,hsl(var(--primary)/.3)_0%,hsl(var(--primary))_50%,hsl(var(--primary)/.3)_100%)]" />
                    <div className="inline-flex h-full w-full cursor-pointer items-center justify-center rounded-full bg-background text-xs font-medium backdrop-blur-3xl">
                      {onCtaClick ? (
                        <button
                          onClick={onCtaClick}
                          className="inline-flex rounded-full text-center group items-center w-full justify-center bg-gradient-to-tr from-muted/40 via-primary/30 to-transparent text-foreground border-input border-[1px] hover:bg-gradient-to-tr hover:from-muted/50 hover:via-primary/40 hover:to-transparent transition-all sm:w-auto py-4 px-10"
                        >
                          {ctaText}
                        </button>
                      ) : (
                        <a
                          href={ctaHref}
                          className="inline-flex rounded-full text-center group items-center w-full justify-center bg-gradient-to-tr from-muted/40 via-primary/30 to-transparent text-foreground border-input border-[1px] hover:bg-gradient-to-tr hover:from-muted/50 hover:via-primary/40 hover:to-transparent transition-all sm:w-auto py-4 px-10"
                        >
                          {ctaText}
                        </a>
                      )}
                    </div>
                  </span>
                </div>
              </div>
              {bottomImage && (
                <div className="mt-16 md:mt-32 mx-2 sm:mx-6 md:mx-10 relative z-10">
                  <img
                    src={bottomImage.light}
                    className="w-full shadow-lg rounded-lg border border-border light:block dark:hidden"
                    alt="Dashboard preview"
                  />
                  <img
                    src={bottomImage.dark}
                    className="hidden w-full shadow-lg rounded-lg border border-border dark:block"
                    alt="Dashboard preview"
                  />
                </div>
              )}
            </motion.div>
          </motion.section>
        </div>
      </div>
    )
  },
)
HeroSection.displayName = "HeroSection"

export { HeroSection }
